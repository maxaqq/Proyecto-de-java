    void acceder(String usuario,String passs) {
     String sql = "SELECT * FROM RegistroUsuarios WHERE Usuario ='"+usuario+"' && Pass='"+passs+"'";
     String cap = "";
     try{     
     Statement st = cn.createStatement();
     ResultSet rs = st.executeQuery(sql);
     while(rs.next())
     {
      cap = rs.getString("Tipo");
      
     }
      if(cap.equals("Administrador"))
      {
        MenuAdmin MP3 = new MenuAdmin();
        MP3.setVisible(true);
        this.dispose();
      }
      else if(cap.equals("Usuario"))
      {
         MenuUsuario MU = new MenuUsuario();
         MU.setVisible(true);
          this.dispose();
      }
     }catch(SQLException ex){
          Logger.getLogger(IngresarPrograma.class.getName()).log(Level.SEVERE, null, ex);
     System.out.println("error al ingresar" + ex);
     }
     
 }
